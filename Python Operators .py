#Python Operators
# There are different types of operators such as ;


# 1) Logical 

# a)The AND operator in Python is represented by the symbol 'and', and it returns True if both operands are True,
#   otherwise it returns False.
# b)The OR operator in Python is represented by the symbol 'or', and it returns True if at least one of the
#   operands is True, otherwise it returns False.
# c)The NOT operator in Python is represented by the symbol 'not', and it returns the opposite boolean value of
#   the operand. If the operand is True, it returns False, and if the operand is False, it returns True.
# d)The IN operator in Python is used to check if a value exists in a sequence, and it returns True if the
#   value is found, otherwise it returns False.
# e)The NOT IN operator in Python is used to check if a value does not exist in a sequence, and it returns 
#   True if the value is not found, otherwise it returns False.
# f)The IS operator in Python is used to compare object identities, and it returns True if both objects have
#   the same identity, otherwise it returns False.
# g)The IS NOT operator in Python is used to compare object identities, and it returns True if both objects have 
#   different identities, otherwise it returns False.
# h)The EQUALS TO operator in Python is represented by the symbol '==', and it returns True if both operands
#   have the same value, otherwise it returns False.
# i)The NOT EQUAL TO operator in Python is represented by the symbol '!=', and it returns True if both operands 
#   have different values, otherwise it returns False.
# j)The GREATER THAN operator in Python is represented by the symbol '>', and it returns True if the left
#   operand is greater than the right operand, otherwise it returns False.
# k)The LESS THAN operator in Python is represented by the symbol '<', and it returns True if the left operand
#   is less than the right operand, otherwise it returns False.
# l)The GREATER THAN OR EQUAL TO operator in Python is represented by the symbol '>=', and it returns True 
#   if the left operand is greater than or equal to the right operand, otherwise it returns False.
# m)The LESS THAN OR EQUAL TO operator in Python is represented by the symbol '<=', and it returns True if
#   the left operand is less than or equal to the right operand, otherwise it returns False.





# 2) Equality

# a)The equality operator (==) compares the values of two operands and returns true if they are equal,
#   otherwise it returns false.
# b)The not equal operator (!=) compares the values of two operands and returns true if they are not equal,
#    otherwise it returns false.
# c)The identity operator (is) checks if two operands refer to the same object in memory and returns 
#   true if they do, otherwise it returns false.
# d)The not identity operator (is not) checks if two operands do not refer to the same object in memory
#    and returns true if they do not, otherwise it returns false.
# e)The membership operator (in) checks if a value is a member of a sequence and returns true if it is,
#    otherwise it returns false.
# f)The not membership operator (not in) checks if a value is not a member of a sequence and returns true
#    if it is not, otherwise it returns false.




# 3) Comparision

# a)The greater than operator (>) returns true if the left operand is greater than the right operand, 
#   otherwise it returns false.
# b)The less than operator (<) returns true if the left operand is less than the right operand, 
#   otherwise it returns false.
# c)The greater than or equal to operator (>=) returns true if the left operand is greater than or equal to 
#   the right operand, otherwise it returns false.
# d)The less than or equal to operator (<=) returns true if the left operand is less than or equal to the
#    right operand, otherwise it returns false.
# e)The not equal operator (!=) compares the values of two operands and returns true if they are not equal, 
#   otherwise it returns false.
# f)The equality operator (==) compares the values of two operands and returns true if they are equal,
#    otherwise it returns false.





# 4) Arithmetic 
# a)The addition operator (+) adds the values of two operands.
# b)The subtraction operator (-) subtracts the value of the right operand from the 
#   value of the left operand.
# c)The multiplication operator (*) multiplies the values of two operands.
# d)The division operator (/) divides the value of the left operand by the value of the right operand.
# e)The modulus operator (%) returns the remainder of the division of the left operand by the right operand.
# f)The floor division operator (//) divides the value of the left operand by the value of the right
#   operand and returns the integer quotient (rounded down to the nearest integer).
# g)The exponentiation operator (**) raises the value of the left operand to the power of the value of the
#    right operand.
